export abstract class AppSettings {

  // local storage identifier for search mode
  public static readonly LocalStorageSearchMode = 'SearchMode';

  // local storage identifier for search and filter input
  public static readonly LocalStorageSearchContext = 'SearchContext';

  // local storage identifier for hiding the keyboard when return to search component
  public static readonly LocalStorageHideKeyboard = 'HideKeyboard';

  // entry detail component identifier
  public static readonly EntryDetailComponentName = 'EntryDetailComponent';
}
